#include "libft.h"

int		ft_atoi(const char *str)
{
	int		ans;
	int		neg;

	ans = 0;
	neg = 1;
	while (*str == ' ' || *str == '\t' || *str == '\n'
			|| *str == '\v' || *str == '\r' || *str == '\f')
		str++;
	if (*str == '-')
		neg = -1;
	if (*str == '+' || *str == '-')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		ans = (ans * 10 + *str - '0');
		str++;
	}
	return (ans * neg);
}
